package com.java.Collections;

public class SetIn {
	String name;
	int marks;
	int age;
	public SetIn(String name, int marks, int age) {
		super();
		this.name = name;
		this.marks = marks;
		this.age = age;
	}
	@Override
	public String toString() {
		return "SetIn [name=" + name + ", marks=" + marks + ", age=" + age + "]";
	}
	
}
